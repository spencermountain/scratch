const temperatureData = function() {
  const noise = 14;
  let arr = [];
  for(let i = 0; i < 365; i += 1) {
    let month = Math.floor(i / 31);
    let val = Math.random() * noise;
    val += minTemp[month];
    val = parseInt(val, 10);
    arr.push(val);
  }
  arr[240] = 38;
  arr[15] = -35;
  return arr;
};

